const data = require('./data');

function animalsByIds(...ids) {
  if(ids.length === 0) {
    return [];
  }
  let find = [];
  ids.forEach((id) => {
    data.animals.find((element) => {
      if(element.id === id) {
        find.push(element);
      }
    });
  });
  return find;
}

